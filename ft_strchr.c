#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i] != c)
	{
		if (i == ft_strlen((char*)s) + 1)
			return (NULL);
		i++;
	}
	return (&((char*)s)[i]);
}
