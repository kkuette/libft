int		ft_nbrlen(int nb)
{
	int result;

	result = 0;
	if (nb < 0)
	{
		result++;
		nb *= -1;
	}
	if (nb == 0)
		result = 1;
	while (nb > 0)
	{
		nb = nb / 10;
		result++;
	}
	return (result);
}
